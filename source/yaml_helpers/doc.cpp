//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
Doc::Doc( const String& file )
{
    std::fstream inStream( file );
    YAML::Parser parser( inStream );
    YAML::Node node;
    while ( parser.GetNextDocument( node ) )
    {
        m_nodes.push_back( std::move( node.Clone() ) );
    }
}
